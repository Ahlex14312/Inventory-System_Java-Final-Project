    private void updatebActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebActionPerformed
          if(productIdf.getText().isEmpty()||namef.getText().isEmpty()||quantityf.getText().isEmpty()||descriptionf.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(this, "Missing Information");
       }
       else
       {
             try {

           String productId = productIdf.getText();
           String productName = namef.getText();
           String productQuantity = quantityf.getText();
           String productDescription = descriptionf.getText();
           Class.forName("com.mysql.jdbc.Driver");
           Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/munozpharmacy", "root", "");
           insert = Con.prepareStatement("UPDATE product SET name= ? , quantity= ? , description= ? WHERE id=?");
           insert.setString(1, productName);
           insert.setString(2, productQuantity);
           insert.setString(3, productDescription);
           insert.setString(4, productId);
        
           insert.executeUpdate();
           JOptionPane.showMessageDialog(this, "Succesfully Updated");
           data();
          
       } catch (Exception e) {
           System.out.println(e.getMessage());
       }
       }
    }//GEN-LAST:event_updatebActionPerformed

