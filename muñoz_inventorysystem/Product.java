   public void data() {
       int count;
       try {
           Class.forName("com.mysql.jdbc.Driver");
           Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/munozpharmacy", "root", "");
           insert = Con.prepareStatement("select * from product");
           ResultSet result = insert.executeQuery();
           ResultSetMetaData datas = result.getMetaData();
           count = datas.getColumnCount();
           DefaultTableModel model = (DefaultTableModel) ProductTable.getModel();
           model.setRowCount(0);

           while (result.next()) {
               Vector info = new Vector();

               for (int a = 1; a <= count; a++) {
                   info.add(result.getString("id"));
                   info.add(result.getString("name"));
                   info.add(result.getString("quantity"));
                   info.add(result.getString("description"));
                   info.add(result.getString("category"));
               }
               model.addRow(info);
           }
           Con.close();
       } catch (Exception e) {
           System.out.println(e.getMessage());
       }
   }
