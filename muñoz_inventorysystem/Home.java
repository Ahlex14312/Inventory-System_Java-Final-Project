    private void productMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productMouseClicked
        try {
           Class.forName("com.mysql.jdbc.Driver"); //load the driver
           Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/munozpharmacy", "root", ""); //establishes the connection
           Statement stmt = (Statement) con.createStatement(); //get the connection stream(connection port)  
           ResultSet datas = stmt.executeQuery("select * from `product`");
           datas = stmt.executeQuery("SELECT SUM(`quantity`)FROM `product`");
           datas.next();
           int count = datas.getInt(1);
           prodnum.setText(String.valueOf(count));

           con.close();
       } catch (Exception e) {
           System.out.println(e);
       }
    }//GEN-LAST:event_productMouseClicked

